/*
Write a program to make Simple calculator (to make addition, subtraction, 
multiplication, division and modulo)
 */
#include<stdio.h>
int main()
{
  int a =10;
  int b =20;
    
  printf("the addition of a and b is %d\n",a+b);  
  printf("the summation of a and b is %d\n",a-b);
  printf("the multipalication of a and b is %d\n",a*b);
  printf("the division of a and b is %d\n",b/a);
  printf("the modulus of a and b is %d\n",a%b); 

  return 0;
}