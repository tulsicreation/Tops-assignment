# Q-2: How will you remove last object from a list?
# Suppose list1 is [2, 33, 222, 14, and 25], what is list1 [-1]?

# ans:
# One way is to use the pop() method.
# This method removes the last element of a list by default,
# or you can specify the index of the element you want to remove.
# Another efficient, yet simple approach to delete the last element of the list is by using the del statement.
# The del operator deletes the element at the specified index location from the list.
# To delete the last element, we can use the negative index -1.

# list1[-1] in Python is the last element of the list.
# In this case, list1[-1] would be 25, the last element of the list.
