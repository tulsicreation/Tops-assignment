# Q-14 : What are negative indexes and why are they used?

# ans:
# Negative indexing is used in Python to manipulate sequence objects such as lists, arrays, strings, etc.
# Negative indexing retrieves elements from the end by providing negative numbers as sequence indexes.
# Negative indexes are used to indicate the position of an element within a list or string in reverse order12.
# For instance, if you have to write the list in the opposite order, you can use the negative index2.
# Negative indexes are also used to remove any new-line spaces from the string and allow the string to except the
